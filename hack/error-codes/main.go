// Usage:
//
//	go run hack/error-codes/main.go                 # regenerate all SDKs
//	go run hack/error-codes/main.go --lang=python   # regenerate one SDK
//
// Parses ProxyErrorCode from the proxy source and emits typed constants directly
// into the SDK source trees that consume them. ProxyErrorCode is not part of any
// OpenAPI spec (the proxy is a transparent reverse proxy with no documented API
// surface), so we generate it from Go AST instead.
//
// Daemon, runner, and API codes flow through their respective OpenAPI specs and
// are picked up by the standard openapi-generator pipeline — this tool is
// limited to ProxyErrorCode.
//
// Outputs:
//
//	libs/sdk-python/src/daytona/_generated/proxy_error_code.py
//	libs/sdk-typescript/src/_generated/proxy-error-code.ts
//	libs/sdk-go/pkg/codes/proxy_error_code.go
//	libs/sdk-ruby/lib/daytona/codes/proxy_error_code.rb
//	libs/sdk-java/src/main/java/io/daytona/sdk/codes/ProxyErrorCode.java
//
// CI drift check: run this tool and `git diff --exit-code` on the outputs.

package main

import (
	"fmt"
	"go/ast"
	"go/parser"
	"go/token"
	"os"
	"path/filepath"
	"runtime"
	"strings"
)

type codeEntry struct {
	ConstName string
	Value     string
}

type target struct {
	path  string
	write func(codes []codeEntry, path string)
}

func main() {
	langFlag := ""
	for _, arg := range os.Args[1:] {
		switch {
		case arg == "--lang", arg == "-lang":
			fmt.Fprintln(os.Stderr, "ERROR: --lang requires a value (python|typescript|go|ruby|java|all)")
			os.Exit(2)
		case strings.HasPrefix(arg, "--lang="):
			langFlag = strings.TrimPrefix(arg, "--lang=")
		case strings.HasPrefix(arg, "-lang="):
			langFlag = strings.TrimPrefix(arg, "-lang=")
		}
	}
	if langFlag == "" {
		langFlag = "all"
	}

	repoRoot := findRepoRoot()

	proxyFile := filepath.Join(repoRoot, "apps", "proxy", "pkg", "proxy", "errors.go")
	codes, err := extractCodes(proxyFile, "ProxyErrorCode")
	if err != nil {
		fmt.Fprintf(os.Stderr, "ERROR: %v\n", err)
		os.Exit(1)
	}

	fmt.Printf("Parsed %d ProxyErrorCode constants\n", len(codes))

	allTargets := map[string]target{
		"python": {
			path:  filepath.Join(repoRoot, "libs", "sdk-python", "src", "daytona", "_generated", "proxy_error_code.py"),
			write: writePython,
		},
		"typescript": {
			path:  filepath.Join(repoRoot, "libs", "sdk-typescript", "src", "_generated", "proxy-error-code.ts"),
			write: writeTypescript,
		},
		"go": {
			path:  filepath.Join(repoRoot, "libs", "sdk-go", "pkg", "codes", "proxy_error_code.go"),
			write: writeGo,
		},
		"ruby": {
			path:  filepath.Join(repoRoot, "libs", "sdk-ruby", "lib", "daytona", "codes", "proxy_error_code.rb"),
			write: writeRuby,
		},
		"java": {
			path:  filepath.Join(repoRoot, "libs", "sdk-java", "src", "main", "java", "io", "daytona", "sdk", "codes", "ProxyErrorCode.java"),
			write: writeJava,
		},
	}

	var selected []target
	if langFlag == "all" {
		for _, lang := range []string{"python", "typescript", "go", "ruby", "java"} {
			selected = append(selected, allTargets[lang])
		}
	} else {
		t, ok := allTargets[langFlag]
		if !ok {
			fmt.Fprintf(os.Stderr, "ERROR: unknown --lang %q (expected: python|typescript|go|ruby|java|all)\n", langFlag)
			os.Exit(2)
		}
		selected = []target{t}
	}

	for _, t := range selected {
		if err := os.MkdirAll(filepath.Dir(t.path), 0o755); err != nil {
			fmt.Fprintf(os.Stderr, "ERROR: %v\n", err)
			os.Exit(1)
		}
		t.write(codes, t.path)
	}

	fmt.Println("Done.")
}

func extractCodes(filePath, typeName string) ([]codeEntry, error) {
	fset := token.NewFileSet()
	f, err := parser.ParseFile(fset, filePath, nil, parser.ParseComments)
	if err != nil {
		return nil, err
	}

	var codes []codeEntry
	for _, decl := range f.Decls {
		genDecl, ok := decl.(*ast.GenDecl)
		if !ok || genDecl.Tok != token.CONST {
			continue
		}
		for _, spec := range genDecl.Specs {
			vs, ok := spec.(*ast.ValueSpec)
			if !ok || len(vs.Names) == 0 || len(vs.Values) == 0 {
				continue
			}
			if vs.Type == nil {
				continue
			}
			ident, ok := vs.Type.(*ast.Ident)
			if !ok || ident.Name != typeName {
				continue
			}
			lit, ok := vs.Values[0].(*ast.BasicLit)
			if !ok || lit.Kind != token.STRING {
				continue
			}
			codes = append(codes, codeEntry{
				ConstName: vs.Names[0].Name,
				Value:     strings.Trim(lit.Value, `"`),
			})
		}
	}
	return codes, nil
}

func writeTypescript(codes []codeEntry, path string) {
	var b strings.Builder
	b.WriteString("/*\n")
	b.WriteString(" * AUTO-GENERATED by hack/error-codes/main.go — do not edit.\n")
	b.WriteString(" * Run `go run hack/error-codes/main.go` to regenerate.\n")
	b.WriteString(" *\n")
	b.WriteString(" * Mirrors `apps/proxy/pkg/proxy/errors.go::ProxyErrorCode`.\n")
	b.WriteString(" */\n\n")
	b.WriteString("export enum ProxyErrorCode {\n")
	for _, c := range codes {
		fmt.Fprintf(&b, "  %s = '%s',\n", c.Value, c.Value)
	}
	b.WriteString("}\n")
	mustWrite(path, b.String())
}

func writePython(codes []codeEntry, path string) {
	var b strings.Builder
	b.WriteString("# AUTO-GENERATED by hack/error-codes/main.go — do not edit.\n")
	b.WriteString("# Run `go run hack/error-codes/main.go` to regenerate.\n")
	b.WriteString("#\n")
	b.WriteString("# Mirrors `apps/proxy/pkg/proxy/errors.go::ProxyErrorCode`.\n\n")
	b.WriteString("from enum import Enum\n\n\n")
	b.WriteString("class ProxyErrorCode(str, Enum):\n")
	if len(codes) == 0 {
		b.WriteString("    pass\n")
	}
	for _, c := range codes {
		fmt.Fprintf(&b, "    %s = \"%s\"\n", c.Value, c.Value)
	}
	mustWrite(path, b.String())
}

func writeGo(codes []codeEntry, path string) {
	var b strings.Builder
	b.WriteString("// Copyright Daytona Platforms Inc.\n")
	b.WriteString("// SPDX-License-Identifier: Apache-2.0\n\n")
	b.WriteString("// Code generated by hack/error-codes/main.go. DO NOT EDIT.\n")
	b.WriteString("// Run `go run hack/error-codes/main.go` to regenerate.\n//\n")
	b.WriteString("// Mirrors apps/proxy/pkg/proxy/errors.go::ProxyErrorCode.\n\n")
	b.WriteString("package codes\n\n")
	b.WriteString("// ProxyErrorCode is the wire-level code emitted by the Daytona proxy when\n")
	b.WriteString("// the error originates inside the proxy itself. Errors from upstream\n")
	b.WriteString("// services flow through unchanged and carry their own source/code.\n")
	b.WriteString("type ProxyErrorCode string\n\n")
	b.WriteString("const (\n")
	for _, c := range codes {
		fmt.Fprintf(&b, "\t%s ProxyErrorCode = \"%s\"\n", c.ConstName, c.Value)
	}
	b.WriteString(")\n")
	mustWrite(path, b.String())
}

func writeRuby(codes []codeEntry, path string) {
	var b strings.Builder
	b.WriteString("# frozen_string_literal: true\n\n")
	b.WriteString("# AUTO-GENERATED by hack/error-codes/main.go — do not edit.\n")
	b.WriteString("# Run `go run hack/error-codes/main.go` to regenerate.\n")
	b.WriteString("#\n")
	b.WriteString("# Mirrors apps/proxy/pkg/proxy/errors.go::ProxyErrorCode.\n\n")
	b.WriteString("module Daytona\n  module Codes\n    module ProxyErrorCode\n")
	for _, c := range codes {
		fmt.Fprintf(&b, "      %s = '%s'\n", c.Value, c.Value)
	}
	b.WriteString("    end\n  end\nend\n")
	mustWrite(path, b.String())
}

func writeJava(codes []codeEntry, path string) {
	var b strings.Builder
	b.WriteString("// AUTO-GENERATED by hack/error-codes/main.go — do not edit.\n")
	b.WriteString("// Run `go run hack/error-codes/main.go` to regenerate.\n//\n")
	b.WriteString("// Mirrors apps/proxy/pkg/proxy/errors.go::ProxyErrorCode.\n\n")
	b.WriteString("package io.daytona.sdk.codes;\n\n")
	b.WriteString("public enum ProxyErrorCode {\n")
	for i, c := range codes {
		suffix := ","
		if i == len(codes)-1 {
			suffix = ";"
		}
		fmt.Fprintf(&b, "    %s(\"%s\")%s\n", c.Value, c.Value, suffix)
	}
	b.WriteString("\n    private final String value;\n\n")
	b.WriteString("    ProxyErrorCode(String value) { this.value = value; }\n\n")
	b.WriteString("    public String getValue() { return value; }\n\n")
	b.WriteString("    public static ProxyErrorCode fromValue(String value) {\n")
	b.WriteString("        for (ProxyErrorCode code : values()) {\n")
	b.WriteString("            if (code.value.equals(value)) return code;\n")
	b.WriteString("        }\n        return null;\n    }\n}\n")
	mustWrite(path, b.String())
}

func mustWrite(path, content string) {
	if err := os.WriteFile(path, []byte(content), 0o644); err != nil {
		fmt.Fprintf(os.Stderr, "ERROR writing %s: %v\n", path, err)
		os.Exit(1)
	}
	fmt.Printf("  → %s\n", path)
}

func findRepoRoot() string {
	_, thisFile, _, _ := runtime.Caller(0)
	return filepath.Dir(filepath.Dir(filepath.Dir(thisFile)))
}
